
namespace MenuBuddy
{
	/// <summary>
	/// How widgets should be aligned horizontally according to the position
	/// </summary>
	public enum HorizontalAlignment
	{
		/// <summary>
		/// The widget's left edge is placed at the position.
		/// </summary>
		Left,

		/// <summary>
		/// The widget is centered horizontally on the position.
		/// </summary>
		Center,

		/// <summary>
		/// The widget's right edge is placed at the position.
		/// </summary>
		Right
	}
}