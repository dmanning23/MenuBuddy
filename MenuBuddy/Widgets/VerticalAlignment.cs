
namespace MenuBuddy
{
	/// <summary>
	/// How widgets should be aligned vertically according to the position
	/// </summary>
	public enum VerticalAlignment
	{
		/// <summary>
		/// The widget's top edge is placed at the position.
		/// </summary>
		Top,

		/// <summary>
		/// The widget is centered vertically on the position.
		/// </summary>
		Center,

		/// <summary>
		/// The widget's bottom edge is placed at the position.
		/// </summary>
		Bottom
	}
}