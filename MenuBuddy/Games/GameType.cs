namespace MenuBuddy
{
	/// <summary>
	/// Specifies the primary input type used by a game.
	/// </summary>
	public enum GameType
	{
		/// <summary>
		/// Game uses gamepad/controller input.
		/// </summary>
		Controller,

		/// <summary>
		/// Game uses mouse input.
		/// </summary>
		Mouse,

		/// <summary>
		/// Game uses touch screen input.
		/// </summary>
		Touch
	}
}